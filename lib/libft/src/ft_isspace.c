#include "../include/libft.h"

int	ft_isspace(int n)
{
	if ((n == '\n') || (n == '\t') || (n == '\r') || (n == '\v') || (n == '\f')
		|| (n == ' '))
	{
		return (1);
	}
	return (0);
}
